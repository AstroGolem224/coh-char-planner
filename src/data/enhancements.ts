// Helper to get enhancements that match a power's allowed categories
export function getEnhancementsForCategories(allowedCategories: EnhancementCategory[]): Enhancement[] {
  const matching = basicEnhancements.filter((enh) =>
    enh.categories.some((cat) => allowedCategories.includes(cat))
  );

  // Also add set enhancements that match
  for (const set of enhancementSets) {
    if (allowedCategories.includes(set.applicableTo)) {
      matching.push(...set.enhancements);
    }
  }

  return matching;
}

export function getEnhancementById(id: string): Enhancement | undefined {
  const basic = basicEnhancements.find((e) => e.id === id);
  if (basic) return basic;

  for (const set of enhancementSets) {
    const found = set.enhancements.find((e) => e.id === id);
    if (found) return found;
  }
  return undefined;
}

